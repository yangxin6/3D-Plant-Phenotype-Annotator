    @classmethod
    def instance(cls) -> "I18n":
        if cls._instance is None:
            cls._instance = cls()
        return cls._instance

    def set_language(self, lang: str):
        self.lang = lang if lang in ["zh", "en"] else "zh"

    def tr(self, text: str, **kwargs) -> str:
        if not isinstance(text, str):
            return str(text)
        mapped = self._maps.get(self.lang, {}).get(text, text)
        if kwargs:
            try:
                return mapped.format(**kwargs)
            except Exception:
                return mapped
        return mapped
